s = input("Enter your String : ")
print("Type of character present in your string is: ")
if s.isalpha():
	print("Al[phabetical string")
if s.isdigit():
	print("Numeric String")
if s.islower():
	print("LowerCase mein hai poori string::@@::")
if s.isupper():
	print("UpperCase mein hai poori string::@@::")
if s.istitle():
	print("Title case mein hai string aapki sir !")
if s.isspace():
	print("String cntain only spaces")
